package main;

public class WordVO {

	
	private String word;
	private String meaning;
	
	
	public WordVO(String word,String meaning) {
		super();
		this.word=word;
		this.meaning=meaning;
	}
	
	public String getWord() {
		return word;
	}
	
	public String setWord() {
		
		return word;
	}
	
	public String getMeaning() {
		return meaning;
	}
	
	public String setMeaning() {
		return meaning;
	}




	
}
